let targetPoke = null;
function handleCardClick(no) {
  const p = POKEMON_LIST.find(x => String(x.no) === String(no));
  if (!p) return;

  if (state.selectMode) {
    if (state.selectedPokes[p.no]) delete state.selectedPokes[p.no];
    else state.selectedPokes[p.no] = true;
    
    const count = Object.keys(state.selectedPokes).length;
    document.getElementById('selCount').innerText = count;
    document.getElementById('multiBar').classList.toggle('open', count > 0);
    renderGrid();
  } else {
    targetPoke = p;
    openModal(p);
  }
}

function assignMulti(areaIdx) {
  Object.keys(state.selectedPokes).forEach(no => {
    if (areaIdx === -1) delete state.assignments[no];
    else state.assignments[no] = areaIdx;
  });
  save();
  toggleSelectMode();
}

