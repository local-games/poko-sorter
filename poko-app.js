const state = {
  currentTab: -1, // -1:すべて, -2:未割り当て, 0-4:各エリア
  assignments: {}, // no -> areaIdx
  searchQuery: '',
  selectedJobs: [],
  selectedEnvs: [],
  selectMode: false,
  selectedPokes: {}, // no -> true
  areaNames: [...INITIAL_NAMES]
};

// 初期化
function init() {
  const saved = localStorage.getItem('pokopoke_assign_v2');
  if (saved) {
    try {
      const parsed = JSON.parse(saved);
      state.assignments = parsed.assignments || {};
      if (parsed.areaNames && parsed.areaNames.length === 5) state.areaNames = parsed.areaNames;
    } catch(e) {}
  }
  buildFilterChips();
  render();
}

function save() {
  localStorage.setItem('pokopoke_assign_v2', JSON.stringify({
    assignments: state.assignments,
    areaNames: state.areaNames
  }));
}

// フィルター用チップ構築
function buildFilterChips() {
  const jobSet = new Set();
  for (let k in POKEMON_JOBS) POKEMON_JOBS[k].forEach(j => jobSet.add(j));
  document.getElementById('jobChips').innerHTML = Array.from(jobSet).map(j => 
    `<button class="filter-chip" data-job="${j}" onclick="toggleJobFilter('${j}')">${j}</button>`
  ).join('');

  const envSet = new Set();
  for (let k in POKEMON_META) envSet.add(POKEMON_META[k][0]);
  document.getElementById('envChips').innerHTML = Array.from(envSet).map(e => 
    `<button class="filter-chip env-chip" data-env="${e}" onclick="toggleEnvFilter('${e}')">${e}</button>`
  ).join('');
}

function toggleFilterPanel() {
  document.getElementById('filterPanel').classList.toggle('open');
  document.getElementById('filterBtn').classList.toggle('active');
}

function toggleJobFilter(job) {
  const idx = state.selectedJobs.indexOf(job);
  if (idx > -1) state.selectedJobs.splice(idx, 1);
  else state.selectedJobs.push(job);
  document.querySelector(`[data-job="${job}"]`).classList.toggle('active');
  render();
}

function toggleEnvFilter(env) {
  const idx = state.selectedEnvs.indexOf(env);
  if (idx > -1) state.selectedEnvs.splice(idx, 1);
  else state.selectedEnvs.push(env);
  document.querySelector(`[data-env="${env}"]`).classList.toggle('active');
  render();
}

function clearFilters() {
  state.selectedJobs = [];
  state.selectedEnvs = [];
  document.querySelectorAll('.filter-chip').forEach(c => c.classList.remove('active'));
  render();
}

function toggleSelectMode() {
  state.selectMode = !state.selectMode;
  state.selectedPokes = {};
  document.getElementById('selBtn').classList.toggle('active', state.selectMode);
  if (!state.selectMode) document.getElementById('multiBar').classList.remove('open');
  renderGrid();
}

